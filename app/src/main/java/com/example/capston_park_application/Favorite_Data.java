package com.example.capston_park_application;

public class Favorite_Data {
    private String favorite_name;
    private String favorite_address_new;
    private String favorite_address_old;
    private String parkinglot_id;
    private String capacity;

    public String getFavorite_name() {
        return favorite_name;
    }

    public void setFavorite_name(String favorite_name) {
        this.favorite_name = favorite_name;
    }

    public String getFavorite_address_new() {
        return favorite_address_new;
    }

    public void setFavorite_address_new(String favorite_address_new) {
        this.favorite_address_new = favorite_address_new;
    }

    public String getFavorite_address_old() {
        return favorite_address_old;
    }

    public void setFavorite_address_old(String favorite_address_old) {
        this.favorite_address_old = favorite_address_old;
    }

    public String getParkinglot_id() {
        return parkinglot_id;
    }

    public void setParkinglot_id(String parkinglot_id) {
        this.parkinglot_id = parkinglot_id;
    }

    public String getCapacity() {
        return capacity;
    }

    public void setCapacity(String capacity) {
        this.capacity = capacity;
    }
}
